typedef enum {
	E_FATAL	= 0,
	E_ERR	= 1,
	E_WARN	= 2,
	E_INFO	= 3,
	E_TRACE	= 4,
} E_LEVEL;
#define EFMT(FMT)	"Where: __FILE__:= |%s|, __LINE_NO__ = %d\nDetails: " FMT "\n", __FILE__, __LINE__
void	eprintf(E_LEVEL, const char* format, ...);


